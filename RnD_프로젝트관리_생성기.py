def create_checklists(base_path):
    """체크리스트 파일 생성"""
    for folder_name, content in CHECKLISTS.items():
        checklist_path = base_path / folder_name / "CHECKLIST.md"
        with open(checklist_path, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"  [+] {folder_name}/CHECKLIST.md")


def create_templates(base_path):
    """템플릿 파일 생성"""
    template_folder = base_path / "00_templates"
    for filename, content in TEMPLATES.items():
        template_path = template_folder / filename
        with open(template_path, 'w', encoding='utf-8') as f:
            f.write(content)
        print(f"  [+] 00_templates/{filename}")


